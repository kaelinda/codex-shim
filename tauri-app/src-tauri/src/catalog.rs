fn default_context(model: &ShimModel) -> i64 {
    let lower = format!("{} {}", model.model, model.display_name).to_lowercase();
    if lower.contains("claude") || lower.contains("deepseek") {
        200_000
    } else if lower.contains("gpt-5") {
        400_000
    } else if lower.contains("gemini") {
        1_000_000
    } else {
        128_000
    }
}

fn supports_reasoning_summaries(model: &ShimModel) -> bool {
    matches!(model.provider.as_str(), "deepseek" | "anthropic" | "minimax" | "dashscope" | "volcengine")
        || (model.provider == "moonshot" && model.model.starts_with("kimi-"))
}

fn reasoning_effort(model: &ShimModel) -> &'static str {
    let lower = model.display_name.to_lowercase();
    if lower.contains("xhigh") || lower.contains("x-high") {
        "xhigh"
    } else if lower.contains("high") {
        "high"
    } else if lower.contains("medium") {
        "medium"
    } else if lower.contains("low") {
        "low"
    } else {
        "medium"
    }
}

pub fn toml_escape(value: &str) -> String {
    value.replace('\\', "\\\\").replace('"', "\\\"")
}
